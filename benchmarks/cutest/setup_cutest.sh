#!/usr/bin/env bash
# Install the CUTEst toolchain (SIFDecode + CUTEst + MASTSIF) into
# $CUTEST_ROOT (default ~/.local/cutest). Idempotent: skips steps whose
# outputs already exist.
#
# Usage:
#   bash setup_cutest.sh                   # install to ~/.local/cutest
#   CUTEST_ROOT=/path/to/cutest bash setup_cutest.sh
#
# After this runs, build.rs and prepare.sh both pick up the install
# automatically from $CUTEST_ROOT (which defaults to ~/.local/cutest, so
# nothing extra to source).
#
# Requirements:
#   - git
#   - meson + ninja  (pip install meson ninja, or brew/apt)
#   - gfortran (brew install gcc / apt install gfortran)
#   - C compiler

set -euo pipefail

CUTEST_ROOT="${CUTEST_ROOT:-${HOME}/.local/cutest}"
PREFIX="${CUTEST_ROOT}/install"

SIFDECODE_REPO="https://github.com/ralna/SIFDecode.git"
CUTEST_REPO="https://github.com/ralna/CUTEst.git"
MASTSIF_REPO="https://bitbucket.org/optrove/sif.git"

SIFDECODE_DIR="${CUTEST_ROOT}/sifdecode"
CUTEST_DIR="${CUTEST_ROOT}/cutest"
MASTSIF_DIR="${CUTEST_ROOT}/mastsif"

echo "CUTEST_ROOT=${CUTEST_ROOT}"
echo "PREFIX=${PREFIX}"

# ---------------------------------------------------------------------------
# Verify toolchain
# ---------------------------------------------------------------------------
MISSING=()
for tool in git meson ninja gfortran; do
    command -v "$tool" &>/dev/null || MISSING+=("$tool")
done
if [[ ${#MISSING[@]} -gt 0 ]]; then
    echo "ERROR: missing required tools: ${MISSING[*]}"
    echo "  meson + ninja:  pip install meson ninja  (or brew install meson ninja)"
    echo "  gfortran:       brew install gcc  (macOS) / apt install gfortran (Linux)"
    exit 1
fi

mkdir -p "${CUTEST_ROOT}" "${PREFIX}"

# ---------------------------------------------------------------------------
# 1. SIFDecode
# ---------------------------------------------------------------------------
if [[ ! -x "${PREFIX}/bin/sifdecoder" ]]; then
    if [[ ! -d "${SIFDECODE_DIR}/.git" ]]; then
        echo "Cloning SIFDecode..."
        git clone --depth 1 "${SIFDECODE_REPO}" "${SIFDECODE_DIR}"
    fi
    echo "Building SIFDecode..."
    (
        cd "${SIFDECODE_DIR}"
        rm -rf builddir
        meson setup builddir --prefix="${PREFIX}"
        meson compile -C builddir
        meson install -C builddir
    )
else
    echo "SIFDecode already installed at ${PREFIX}/bin/sifdecoder"
fi

# ---------------------------------------------------------------------------
# 2. CUTEst
# ---------------------------------------------------------------------------
if [[ ! -f "${PREFIX}/lib/libcutest_double.a" ]]; then
    if [[ ! -d "${CUTEST_DIR}/.git" ]]; then
        echo "Cloning CUTEst..."
        git clone --depth 1 "${CUTEST_REPO}" "${CUTEST_DIR}"
    fi
    echo "Building CUTEst..."
    (
        cd "${CUTEST_DIR}"
        rm -rf builddir
        meson setup builddir --prefix="${PREFIX}" -Dtests=false
        meson compile -C builddir
        meson install -C builddir
    )
else
    echo "CUTEst already installed at ${PREFIX}/lib/libcutest_double.a"
fi

# Sanity-check: build.rs needs cutest_trampoline.f90 from the CUTEst source tree
TRAMPOLINE_SRC="${CUTEST_DIR}/src/tools/cutest_trampoline.f90"
if [[ ! -f "${TRAMPOLINE_SRC}" ]]; then
    echo "WARNING: ${TRAMPOLINE_SRC} not found; build.rs will fail."
    echo "  The CUTEst source tree must remain at ${CUTEST_DIR}."
fi

# ---------------------------------------------------------------------------
# 3. MASTSIF
# ---------------------------------------------------------------------------
if ! ls "${MASTSIF_DIR}"/*.SIF &>/dev/null 2>&1; then
    if [[ ! -d "${MASTSIF_DIR}/.git" ]]; then
        echo "Cloning MASTSIF..."
        git clone --depth 1 "${MASTSIF_REPO}" "${MASTSIF_DIR}"
    fi
else
    SIF_COUNT=$(ls "${MASTSIF_DIR}"/*.SIF 2>/dev/null | wc -l | tr -d ' ')
    echo "MASTSIF already present at ${MASTSIF_DIR} (${SIF_COUNT} SIF files)"
fi

# ---------------------------------------------------------------------------
# 4. Write env.sh for back-compat / interactive use
# ---------------------------------------------------------------------------
cat > "${CUTEST_ROOT}/env.sh" <<EOF
# CUTEst environment - generated by benchmarks/cutest/setup_cutest.sh
# Sourcing is optional: prepare.sh and build.rs read \$CUTEST_ROOT directly.
export CUTEST_ROOT="${CUTEST_ROOT}"
export CUTEST="${PREFIX}"
export SIFDECODE="${PREFIX}"
export MASTSIF="${MASTSIF_DIR}"
export PATH="${PREFIX}/bin:\$PATH"
export LD_LIBRARY_PATH="${PREFIX}/lib:\${LD_LIBRARY_PATH:-}"
export DYLD_LIBRARY_PATH="${PREFIX}/lib:\${DYLD_LIBRARY_PATH:-}"
EOF

echo ""
echo "Done. CUTEst toolchain installed at ${PREFIX}."
echo "Next: bash $(dirname "$0")/prepare.sh ROSENBR    # quick smoke test"
